def _print_status(g: Game) -> None:
    r = g.realm
    print()
    print(f"  Year {g.year} | {g.realm.name} | Ruler: {g.ruler.title} "
          f"[{', '.join(g.ruler.traits)}]")
    print(f"  Gold {r.treasury}g | Tax {r.tax_rate}% | Army {r.army} | "
          f"Pop {r.population}")
    print(f"  Prosperity {r.prosperity} | Stability {r.stability} | "
          f"Legitimacy {r.legitimacy}")
    heirs = _living(g, "child") + _living(g, "sibling")
    print("  Heirs: " + (", ".join(h.title for h in heirs) if heirs else "NONE!"))
    print("  Neighbours: " + ", ".join(
        f"{n.name}({n.relation:+d}{',WAR' if n.at_war else ''})"
        for n in g.neighbors))


def _ask_human(prompt: str, options: list) -> int:
    print("\n" + prompt)
    for i, label in enumerate(options):
        print(f"   [{i + 1}] {label}")
    while True:
        raw = input("   > ").strip()
        if raw.isdigit() and 1 <= int(raw) <= len(options):
            return int(raw) - 1
        print(f"   Enter 1-{len(options)}.")


def play(seed: int | None = None) -> None:
    if seed is None:
        seed = random.randint(0, 10**9)
    g = new_game(seed)
    print("=" * 64)
    print(f"  REALM — you are {g.ruler.name}, ruler of {g.realm.name}.")
    print("  Keep your dynasty on the throne. (Ctrl+C to abdicate.)")
    print("=" * 64)
    try:
        while not g.over:
            _print_status(g)
            process_turn(g, _ask_human)
            for line in g.log:
                if line.startswith(("EVENT", "DECREE", "  >")) or "===" not in line:
                    print(line)
        _print_status(g)
        print("\n*** " + g.over_reason + " ***")
        print(f"Your dynasty ruled {g.realm.name} for {g.turn} years.")
        print(f"Final prestige: {prestige_score(g)}")
    except (KeyboardInterrupt, EOFError):
        print(f"\nYou abdicate the throne after {g.turn} years. "
              f"Prestige: {prestige_score(g)}. Farewell.")


def main(argv: list) -> int:
    if len(argv) > 1 and argv[1] == "--selfplay":
        seeds = int(argv[2]) if len(argv) > 2 else 10
        turns = int(argv[3]) if len(argv) > 3 else 30
        return 0 if run_harness(seeds, turns) else 1
    seed = int(argv[2]) if len(argv) > 2 and argv[1] == "--seed" else None
    play(seed)
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv))
